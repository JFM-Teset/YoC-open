#include <stdint.h>
#include <string.h>
#include <stdbool.h>
#include <aos/kernel.h>
#include "bt_app_core.h"
#include <aos/debug.h>

#include <aos/log.h>

static void bt_app_task_handler(void *arg);
static bool bt_app_send_msg(bt_app_msg_t *msg);
static void bt_app_work_dispatched(bt_app_msg_t *msg);

static aos_queue_t s_bt_app_task_queue;
static aos_task_t s_bt_app_task_handle;
static char queue_buf[10 * sizeof(bt_app_msg_t)];

bool bt_app_work_dispatch(bt_app_cb_t p_cback, uint16_t event, void *p_params, int param_len, bt_app_copy_cb_t p_copy_cback)
{
    LOGD(BT_APP_CORE_TAG, "%s event 0x%x, param len %d", __func__, event, param_len);

    bt_app_msg_t msg;
    memset(&msg, 0, sizeof(bt_app_msg_t));

    msg.sig = BT_APP_SIG_WORK_DISPATCH;
    msg.event = event;
    msg.cb = p_cback;

    if (param_len == 0) {
        return bt_app_send_msg(&msg);
    } else if (p_params && param_len > 0) {
        if ((msg.param = malloc(param_len)) != NULL) {
            memcpy(msg.param, p_params, param_len);
            /* check if caller has provided a copy callback to do the deep copy */
            if (p_copy_cback) {
                p_copy_cback(&msg, msg.param, p_params);
            }
            return bt_app_send_msg(&msg);
        }
    }

    return false;
}

static bool bt_app_send_msg(bt_app_msg_t *msg)
{
    if (msg == NULL) {
        return false;
    }

    if (aos_queue_send(&s_bt_app_task_queue, msg, sizeof(bt_app_msg_t)) != 0) {
        LOGE(BT_APP_CORE_TAG, "%s xQueue send failed", __func__);
        return false;
    }
    return true;
}

static void bt_app_work_dispatched(bt_app_msg_t *msg)
{
    if (msg->cb) {
        msg->cb(msg->event, msg->param);
    }
}

static void bt_app_task_handler(void *arg)
{
    bt_app_msg_t msg;
    uint32_t len;
    for (;;) {
        if (0 == aos_queue_recv(&s_bt_app_task_queue, AOS_WAIT_FOREVER, &msg, &len)) {
            LOGD(BT_APP_CORE_TAG, "%s, sig 0x%x, 0x%x", __func__, msg.sig, msg.event);
            switch (msg.sig) {
            case BT_APP_SIG_WORK_DISPATCH:
                bt_app_work_dispatched(&msg);
                break;
            default:
                LOGW(BT_APP_CORE_TAG, "%s, unhandled sig: %d", __func__, msg.sig);
                break;
            } // switch (msg.sig)

            if (msg.param) {
                free(msg.param);
            }
        }
    }
}

void bt_app_task_start_up(void)
{
    int ret  = aos_queue_new(&s_bt_app_task_queue, queue_buf, 10 * sizeof(bt_app_msg_t), sizeof(bt_app_msg_t));
    aos_check(!ret, ERR_MEM);
    ret = aos_task_new_ext(&s_bt_app_task_handle, "BtAppT", bt_app_task_handler, NULL, 2048, AOS_DEFAULT_APP_PRI);
    aos_check(!ret, ERR_MEM);
    return;
}

