static uint8_t Digest[32] = {
    0xe4, 0x2c, 0x9f, 0x12, 0xf7, 0xd2, 0x67, 0x3a, 0x23, 0xea, 0x85, 0x61, 0xeb, 0xb2, 0xc2, 0x19,
    0xdc, 0xd9, 0xf1, 0xaa
};

uint8_t dst[1024 >> 3] = {0};
uint32_t dst_size = 0;

uint8_t dst_2048[2048 >> 3] = {0};

void tee_rsa_test(void)
{
    int ret;

    TEE_LOGI("\r\n%s\n", __func__);

    TEE_HEX_DUMP("RSA1024 MD5 digest:", Digest, 16);

    ret = csi_tee_cid_rsa_sign(Digest, 16, dst, &dst_size, TEE_RSA_MD5);
    TEE_HEX_DUMP("RSA1024 MD5 sign:", dst, dst_size);

    if (ret == 0) {
        ret = csi_tee_cid_rsa_verify(Digest, 16, dst, dst_size, TEE_RSA_MD5);
        TEE_LOGI("rsa md5 sign and verify: %s\n", ret ? "Fail" : "Pass");
    } else {
        TEE_LOGI("rsa md5 sign: FAIL ret %x\n", ret);
    }

    TEE_HEX_DUMP("RSA1024 SHA1 digest:", Digest, 20);

    ret = csi_tee_cid_rsa_sign(Digest, 20, dst, &dst_size, TEE_RSA_SHA1);
    TEE_HEX_DUMP("RSA1024 SHA1 sign:", dst, dst_size);

    if (ret == 0) {
        ret = csi_tee_cid_rsa_verify(Digest, 20, dst, dst_size, TEE_RSA_SHA1);
        TEE_LOGI("rsa sha1 sign and verify: %s\n", ret ? "Fail" : "Pass");
    } else {
        TEE_LOGI("rsa sha1 sign: FAIL ret %x\n", ret);
    }

    TEE_HEX_DUMP("RSA1024 encrypt digest:", Digest, 20);

    ret = csi_tee_cid_rsa_encrypt(Digest, 20, dst, &dst_size, TEE_RSA_PKCS1_PADDING);

    TEE_HEX_DUMP("RSA1024 encrypt:", dst, dst_size);

    if (ret == 0) {
        ret = csi_tee_cid_rsa_decrypt(dst, dst_size, dst, &dst_size, TEE_RSA_PKCS1_PADDING);
        TEE_HEX_DUMP("RSA1024 decrypt:", dst, dst_size);

        if (ret == 0) {
            TEE_LOGI("rsa encrypt and decrypt: %s\n", memcmp(dst, Digest, 20) ? "Fail" : "Pass");
        }
    }

    ret = csi_tee_rsa_sign(Digest, 32,
                           rsa2048_key, sizeof(rsa2048_key),
                           dst_2048, &dst_size,
                           TEE_RSA_SHA256);

    TEE_HEX_DUMP("RSA2048 SHA256 sign:", dst_2048, dst_size);

    if (ret) {
        TEE_LOGI("RSA2048 sign Fail,ret %x", ret);
    }

    ret = csi_tee_rsa_verify(Digest, 32,
                             rsa2048_pub_key, sizeof(rsa2048_pub_key),
                             dst_2048, dst_size,
                             TEE_RSA_SHA256);
    TEE_LOGI("RSA2048 SHA256 sign and verify: %s\n", ret ? "Fail" : "Pass");

    ret =  csi_tee_rsa_encrypt(Digest, 32,
                               rsa2048_pub_key, sizeof(rsa2048_pub_key),
                               dst_2048, &dst_size,
                               TEE_RSA_PKCS1_PADDING);
    TEE_HEX_DUMP("RSA2048 encrypt:", dst_2048, dst_size);

    if (ret) {
        TEE_LOGI("RSA2048 encrypt Fail,ret %x", ret);
    }

    ret = csi_tee_rsa_decrypt(dst_2048, dst_size,
                              rsa2048_key, sizeof(rsa2048_key),
                              dst_2048, &dst_size,
                              TEE_RSA_PKCS1_PADDING);
    TEE_HEX_DUMP("RSA2048 decrypt:", dst_2048, dst_size);

    if (ret == 0) {
        TEE_LOGI("RSA2048 encrypt and decrypt: %s\n", memcmp(dst_2048, Digest, 20) ? "Fail" : "Pass");
    } else {
        TEE_LOGI("RSA2048 decrypt Fail,ret %x", ret);
    }
}

